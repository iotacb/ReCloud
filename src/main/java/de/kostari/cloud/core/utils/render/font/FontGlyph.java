package de.kostari.cloud.core.utils.render.font;

public class FontGlyph {

    public int x;
    public int y;
    public int width;
    public int height;

    public FontGlyph(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
